void reorder(vector<int> &inp,int l,int r, int command) {
    vector<int> tmp(inp.begin()+l,inp.begin()+r);
    inp.erase(inp.begin()+l,inp.begin()+r);
    if(command==1){
        inp.insert(inp.begin(),tmp.begin(),tmp.end());
    }else if(command==2) {
        inp.insert(inp.end(),tmp.begin(),tmp.end());
    }
}

